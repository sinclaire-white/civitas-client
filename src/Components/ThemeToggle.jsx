import { useState, useEffect } from 'react';

const ThemeToggle = () => {
  const [theme, setTheme] = useState(
    localStorage.getItem('theme') || 'light'
  );

  useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
  }, [theme]);

  const toggleTheme = () => {
    setTheme((prev) => (prev === 'light' ? 'dark' : 'light'));
  };

  return (
     <button
      onClick={toggleTheme}
      className="btn btn-ghost btn-circle" // DaisyUI button styles
      aria-label="Toggle theme" // Accessibility for screen readers
    >
      {/* Conditionally render Sun or Moon SVG based on current theme */}
      {theme === 'light' ? (
        // Sun Icon (for light theme)
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24" // Increased size for better visibility
          height="24"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
          className="text-yellow-500 stroke-current" // Tailwind color for sun
        >
          <circle cx="12" cy="12" r="5" />
          <path d="M12 1v2M12 21v2M4.2 4.2l1.4 1.4M18.4 18.4l1.4 1.4M1 12h2M21 12h2M4.2 19.8l1.4-1.4M18.4 5.6l1.4-1.4" />
        </svg>
      ) : (
        // Moon Icon (for dark theme)
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24" // Increased size
          height="24"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
          className="text-gray-400 stroke-current" // Tailwind color for moon
        >
          <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z"></path>
        </svg>
      )}
    </button>
  );
};

export default ThemeToggle;
